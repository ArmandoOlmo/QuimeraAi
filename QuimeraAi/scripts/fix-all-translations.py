        (r'^(.+) added successfully[.!]?$', lambda m: f'{m.group(1)} agregado exitosamente'),
        (r'^(.+) removed successfully[.!]?$', lambda m: f'{m.group(1)} removido exitosamente'),
        (r'^Failed to (.+)$', lambda m: f'Error al {m.group(1).lower()}'),
